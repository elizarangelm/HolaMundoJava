public class VarVsPrimitivo {
    public static void main(String[] args) {
        
        /* Para ejecutar este proyecto y ver como se asignan el tipo de dato con
        var se puede pausar la linea, ejecutar debug, y ejecutar linea por linea
        con F8, en la ventana de variables podemos ver esta informacion
        */
        var numeroEntero = 10;  
        System.out.println("numeroEntero = " + numeroEntero);
        
        var numeroDouble = 10.0;  
        System.out.println("numeroDouble = " + numeroDouble);
        
        var numeroFloat = 10.0F;  
        System.out.println("numeroFloat = " + numeroFloat);
               
    }
}
